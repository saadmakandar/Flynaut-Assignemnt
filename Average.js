//take function name average and take parameters 'a' and 'b'
function average(a, b) {
  //return the result of the expression
  //division is the performed before addition
  return a + b / 2;
}

//the value of a is 2 & b is 1
//'2 + 1/2' return the value 2.5.
//print the result
console.log(average(2, 1));

//output : 2.5